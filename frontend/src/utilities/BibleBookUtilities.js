export const checkIfOldTestament = (bookName) => {
    const key = bookName.toLowerCase().replace(/\s+/g, '');
    return bibleBookIdAndTestament[bookName] && bibleBookIdAndTestament[bookName][1] === 'old'
}

export const getOriginalBookNameAndTranslation = (bookName) =>{
    const key = bookName.toLowerCase().replace(/\s+/g, '');
    const originalBookName = bibleBookIdAndTestament[`${bookName}`][2]
    const translatedBookName = bibleBookIdAndTestament[`${bookName}`][3]
    return `${originalBookName} ${translatedBookName}`

}
